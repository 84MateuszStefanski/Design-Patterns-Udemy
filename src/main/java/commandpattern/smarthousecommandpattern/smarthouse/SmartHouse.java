package commandpattern.smarthousecommandpattern.smarthouse;

public class SmartHouse {

    public void openTheGate() {
        System.out.println("Opening the gate ");
    }

    public void openTheDoor() {
        System.out.println("Opening the doors ");
    }

    public void raiseTheBlinds() {
        System.out.println("Rising the blinds ");
    }

    public void turnOnTheLights() {
        System.out.println(" Turning on the lights ");
    }

    public void turnOnTheHeating() {
        System.out.println("Turning on the heating ");
    }

    public void closeTheGate() {
        System.out.println("Closing the gate ");
    }

    public void closeTheDoor() {
        System.out.println("Closing the doors ");
    }

    public void lowerTheBlinds() {
        System.out.println("Lowering the blinds ");
    }

    public void turnOffTheLights() {
        System.out.println(" Turning off the lights ");
    }

    public void turnOffTheHeating() {
        System.out.println("Turning off the heating ");
    }
}
